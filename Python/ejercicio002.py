# Calculo del area y perímetro de un cuadrado
lado = float (input ("Por favor, ingrese la medida del lado del cuadrado: ")) 

# Operaciones
area = lado * lado
perimetro = lado * 4

# Impresión de los resultados
print ("El área del cuadrado es: ") 
print (area)
print ("El perímetro del cuadrado es: ") 
print (perimetro)